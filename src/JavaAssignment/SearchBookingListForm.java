    private void jbtnmodifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnmodifyActionPerformed
        //Select row from the booking table
        int row = bookingtable.getSelectedRow();
        //Open Modify booking form with the selectedid which is the username from row index 0.
        if (row != -1) {
            String selectedid = bookingtable.getValueAt(row, 0).toString();
            ModifyBookingForm mbf = new ModifyBookingForm(selectedid);
            mbf.setLocationRelativeTo(null);
            mbf.setVisible(true);
            dispose();
        } else {
            //If no record is selected then this error will be prompt to the user
            JOptionPane.showMessageDialog(this, "No record is selected", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbtnmodifyActionPerformed

    private void jbtndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtndeleteActionPerformed
        int row = bookingtable.getSelectedRow();
        Booking.deletebooking(bookingtable.getValueAt(row, 0).toString());
        JOptionPane.showMessageDialog(this, "The booking has been deleted !");
        SearchBookingListForm brf = new SearchBookingListForm();
        brf.setLocationRelativeTo(null);
        brf.setVisible(true);
        dispose();
    }//GEN-LAST:event_jbtndeleteActionPerformed

